#DAY3 연산자와 형변환 연습

a=20
b=7

print("%d + %d = %d"%(a,b,a+b)) #덧셈
print("%d - %d = %d"%(a,b,a-b)) #빼기
print("%d * %d = %d"%(a,b,a*b)) #곱하기
print("%f / %f = %f"%(a,b,a/b)) #실수형 나누기
print("%d / %d = %d"%(a,b,a/b)) #정수형 나누기
print("%f // %f = %f"%(a,b,a//b)) #정수까지만 저장되는 나눗셈
print("%d // %d = %d"%(a,b,a//b)) #정수까지만 저장되는 나눗셈
print("%d %% %d = %d"%(a,b,a%b)) #나머지
print("%d ** %d = %d"%(a,b,a**b)) #승수